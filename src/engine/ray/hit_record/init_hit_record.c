#include "ray.h"

void	init_hit_record(t_hit_record *hr)
{
	hr->is_hit = FALSE;
	hr->t = 1000000000;
	hr->point = vec3(0, 0, 0);
	hr->normal = vec3(0, 0, 0);
	hr->is_front = -1;
	hr->rgb = vec3(0, 0, 0);
	hr->mat = 0;
}
